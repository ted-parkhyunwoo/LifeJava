package com.example.javastudy.ch03;

public class AssignmentTest {

    public static void main(String[] args) {
        double num = 3.0;
        num += 3.5; // num = num + 3.5;
        System.out.println(num); // 6.5

        num *= 2; // num = num * 2;
        System.out.println(num); // 13.0
    }
}
